@target('v73', 'xivo-yealink-v73')
def build_v73(path):
    check_call(['rsync', '-rlp', '--exclude', '.*',
                'common/', path])

    check_call(['rsync', '-rlp', '--exclude', '.*',
                'v73/', path])


